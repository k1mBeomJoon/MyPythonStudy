'''
    산술(Arithmetic) 연산자
        숫자 계산을 할 때 사용하는 연산자

'''

a = 7
b = 2
    # 덧셈
print('{} + {} = {}'.format(a, b, a + b))
    # 뺄셈
print('{} - {} = {}'.format(a, b, a - b))
    # 곱셈
print('{} * {} = {}'.format(a, b, a * b))
    # 거듭제곱
print('{} ** {} = {}'.format(a, b, a ** b))
    # 나눗셈
print('{} / {} = {}'.format(a, b, a / b))   # 나눗셈은 기본이 실수범위 연산
    # 몫 구하기
print('{} // {} = {}'.format(a, b, a // b))
    # 나머지 구하기
print('{} % {} = {}'.format(a, b, a % b))